package while_DoWhile;

public class Ciclowhile {
    public static void main(String[] args) {
        System.out.println("Incremento");
        int contador=0;
        while (contador<=3){
            System.out.println(contador);
            contador++;
        }
        System.out.println("Decremento");
        int contador1=8;
        while (contador1>0){
            System.out.println(contador1--);
        }
    }
}
